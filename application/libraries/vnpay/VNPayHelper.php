    /**
     * Hàm format lại chuỗi thời gian thành định dạng thuần Việt
     * Input 20210108155435 -> 08/01/2021 15:54 hoặc 08/01/2021 15:54:35
     *
     * @param [type] $strtime
     * @param string $separate
     * @return void
     */
    function format_yyyymmddhhiiss($strtime, $separate = '/')
    {
        $strtime = preg_replace("/[^0-9]/", "", $strtime);
        if (strlen($strtime) == 14) {
            return preg_replace(
                "/([0-9]{4})([0-9]{2})([0-9]{2})([0-9]{2})([0-9]{2})([0-9]{2})/",
                "$3$separate$2$separate$1 $4:$5",
                $strtime
            );
        } else {
            return $strtime;
        }
    }

